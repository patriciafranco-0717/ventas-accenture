package co.accenture.api.dto;

import javax.validation.constraints.NotEmpty;

public class ClienteDto {

	@NotEmpty(message = "Cedula obligatoria")
	private String cedula;
	
	@NotEmpty(message = "Nombre obligatoria")
	private String nombre;
	
	@NotEmpty(message = "Apellido obligatoria")
	private String apellido;
	
	@NotEmpty(message = "Direccion obligatoria")
	private String direccion;
	
	@NotEmpty(message = "Email obligatoria")
	private String email;
	
	public ClienteDto() {
	}

	/**
	 * @return the cedula
	 */
	public String getCedula() {
		return cedula;
	}

	/**
	 * @param cedula the cedula to set
	 */
	public void setCedula(String cedula) {
		this.cedula = cedula;
	}

	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}

	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	/**
	 * @return the apellido
	 */
	public String getApellido() {
		return apellido;
	}

	/**
	 * @param apellido the apellido to set
	 */
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}

	/**
	 * @return the direccion
	 */
	public String getDireccion() {
		return direccion;
	}

	/**
	 * @param direccion the direccion to set
	 */
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}

	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}
	
	
	
}
